@router.get("/translations")
async def get_translations(
    lang: str = Query("en", description="Language code"),
    namespace: Optional[str] = None,
):
    lang = lang.lower()
    if lang not in TRANSLATIONS:
        return {"lang": lang, "translations": {}, "error": f"Unsupported language: {lang}"}

    translations = TRANSLATIONS[lang]
    if namespace:
        prefix = f"{namespace}."
        translations = {k: v for k, v in translations.items() if k.startswith(prefix) or k == namespace}

    return {"lang": lang, "translations": translations, "count": len(translations)}


