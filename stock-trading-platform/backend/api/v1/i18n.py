@router.get("/numbers/{lang}")
async def get_number_format(lang: str):
    lang = lang.lower()
    if lang not in NUMBER_FORMATS:
        return {"error": f"Unsupported language: {lang}"}
    return {"lang": lang, **NUMBER_FORMATS[lang]}
